x = 3 
y = 2 

hasil = x + y
print(f"Hasil penjumlahan dari {x} + {y} = {hasil}")

hasil = x - y
print(f"Hasil kurang dari {x} - {y} = {hasil}")

hasil = x * y
print(f"Hasil kali dari {x} * {y} = {hasil}")

hasil = x / y
print(f"Hasil bagi dari {x} / {y} = {hasil}")

hasil = x % y
print(f"Hasil modulus (mod) atau sisa pembagian dari {x} % {y} = {hasil}")

hasil = x ** y
print(f"Hasil pangkat dari {x} ^ {y} = {hasil}")


