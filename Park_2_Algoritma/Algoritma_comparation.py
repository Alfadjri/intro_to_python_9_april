a = 10
b = 10

hasil = (a > b)
print(f"Apakah {a} besar dari (>) {b} adalah {hasil} ")
hasil = (a >= b)
print(f"Apakah {a} besar dari sama dengan (>=) {b} adalah {hasil}")

hasil = (a < b)
print(f"Apakah {a} kecil dari (<) {b} adalah {hasil} ")
hasil = (a <= b)
print(f"Apakah {a} kecil dari sama dengan (<=) {b} adalah {hasil}")

hasil = (a == b)
print(f"Apakah {a} sama dengan (==) {b} adalah {hasil}")

c = True
hasil = (c != True)
print(f"Apakah {c} tidak sama (!) dengan True adalah {hasil}")
